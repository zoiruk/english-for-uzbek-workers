/**
 * Check if a key is valid and unused
 * @param {string} key - Key to validate
 * @returns {boolean} - True if valid and unused
 */
window.validateActivationKey = function (key) {
    const cleanKey = key.replace(/-/g, '').toUpperCase();
    const formattedKey = key.toUpperCase();

    // Check if key exists in valid keys
    if (!window.VALID_ACTIVATION_KEYS.includes(cleanKey)) {
        console.log('Key not found in valid keys list:', cleanKey);
        return false;
    }

    // Check if key has been used in local storage
    let isUsedLocally = false;
    try {
        const usedKeys = JSON.parse(localStorage.getItem(window.USED_KEYS_STORAGE_KEY) || '[]');
        isUsedLocally = usedKeys.includes(cleanKey);
    } catch (error) {
        console.warn('Error checking local used keys:', error);
    }

    // Check if key has been used in admin database
    let isUsedInAdmin = false;
    try {
        const adminKeyDatabase = localStorage.getItem('admin_key_database');
        if (adminKeyDatabase) {
            const keyDatabase = JSON.parse(adminKeyDatabase);
            if (keyDatabase[formattedKey] && keyDatabase[formattedKey].activated) {
                isUsedInAdmin = true;
            } else if (keyDatabase[cleanKey] && keyDatabase[cleanKey].activated) {
                isUsedInAdmin = true;
            }
        }
    } catch (error) {
        console.warn('Error checking admin database:', error);
    }

    const isUsed = isUsedLocally || isUsedInAdmin;
    console.log('Global key validation:', {
        key: cleanKey,
        inValidList: true,
        usedLocally: isUsedLocally,
        usedInAdmin: isUsedInAdmin,
        finalResult: !isUsed
    });

    return !isUsed;
};

/**
 * Mark a key as used
 * @param {string} key - Key to mark as used
 * @param {Object} activationDetails - Details about the activation
 */
window.markKeyAsUsed = function (key, activationDetails = {}) {
    const cleanKey = key.replace(/-/g, '').toUpperCase();
    const formattedKey = key.toUpperCase();

    try {
        // Mark in local used keys list
        const usedKeys = JSON.parse(localStorage.getItem(window.USED_KEYS_STORAGE_KEY) || '[]');
        if (!usedKeys.includes(cleanKey)) {
            usedKeys.push(cleanKey);
            localStorage.setItem(window.USED_KEYS_STORAGE_KEY, JSON.stringify(usedKeys));
        }

        // Also mark in admin database if it exists
        try {
            const adminKeyDatabase = localStorage.getItem('admin_key_database');
            if (adminKeyDatabase) {
                const keyDatabase = JSON.parse(adminKeyDatabase);

                // Find the key in admin database and mark as activated
                if (keyDatabase[formattedKey]) {
                    keyDatabase[formattedKey].activated = true;
                    keyDatabase[formattedKey].activationDate = new Date().toISOString();
                    keyDatabase[formattedKey].activationDetails = activationDetails;
                } else if (keyDatabase[cleanKey]) {
                    keyDatabase[cleanKey].activated = true;
                    keyDatabase[cleanKey].activationDate = new Date().toISOString();
                    keyDatabase[cleanKey].activationDetails = activationDetails;
                }

                // Save updated admin database
                localStorage.setItem('admin_key_database', JSON.stringify(keyDatabase));
                console.log('Key marked as used in both systems:', cleanKey);
            }
        } catch (adminError) {
            console.warn('Could not update admin database:', adminError);
        }

    } catch (error) {
        console.error('Error marking key as used:', error);
    }
};

